#!/bin/bash
clear
SECONDS=0
run_script(){
    echo "$1"; eval "$1";
}

###################################################################################################################################################

for n in {10000..10005}; do # 1000..1005
    run_script "python progress_bars.py --n $n"
    :
done

###################################################################################################################################################
mins=$((SECONDS/60))
echo echo "time elapsed=${mins} [mins]"